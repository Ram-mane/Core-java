package com.operator;

public class UnaryOpDemo {

	public static void main(String[] args) {


		int a =10;
		a++;   // executes the increament at end of line
		System.out.println(a++);
		a++;
		System.out.println(a);
		
		System.out.println(a--);
		
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
	}

}
