package com.operator;

public class RelationalOpDemo {

	public static void main(String[] args) {


		// <,>, <=, >=, ==, !=
		
		int a=10;
		int b=5;
		System.out.println(a>b);
		
		System.out.println(a<b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
	}

}
